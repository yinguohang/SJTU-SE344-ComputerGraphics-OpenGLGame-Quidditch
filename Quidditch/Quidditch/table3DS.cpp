void MyMaterial(GLenum mode, GLfloat * f, GLfloat alpha)
{
	GLfloat d[4];
	d[0] = f[0];
	d[1] = f[1];
	d[2] = f[2];
	d[3] = alpha;
	if (mode == GL_AMBIENT) {
		d[0] *= 0.3f;
		d[1] *= 0.3f;
		d[2] *= 0.3f;
	}
	glMaterialfv(GL_FRONT_AND_BACK, mode, d);
}

